package prototype;

/***************************************************************
* Enumerator for the game modes
* There are 3 modes:
* free mode: is where there is no time limits imposed on the users.
* game timed:	The user has a certain amount of time for the game.
* turn timed:	The user has a certain amount of time for a turn.
* @author Gionata Bonazzi
* @author Cole Sellers
* @author Brendan Cronan
* @author Rosa Fleming
* @version stable build 17 April 2018
***************************************************************/
public enum GameMode {
FREE_MODE, GAME_TIMED_MODE, TURN_TIMED_MODE;
}
